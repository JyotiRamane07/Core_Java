package com.jsp.decisionmakingstmt;

public class Switch {
	public static void main(String arg[])
			{
		int choice='a';
		switch(choice)
		{
		case 0 :
		{
			System.out.println(" case 0");
			
		}
		break;
		case 1 :
		{
			System.out.println("case 1 ");
		}
		break;
		default :
		{
			System.out.println(" default ");
	}
  }
 }	
}
